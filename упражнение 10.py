import math
a=int(input("Введите число а "))#ввод числа числа а 
b=int(input("Введите число b "))#ввод числа b 
print("Сумма a и b = ", a+b)
print("Разница между а и b = ", a-b)
print("Произведение a и b = ", a*b)
print("Частное от деления a на b = ", a/b)
print("Остаток от деления a на b = ", a%b)
print("десятичный логарифм числа а = ",math.log10(a))
print("возведение а в степень b = ", a**b)
